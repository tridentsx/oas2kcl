package templates

// IPv4Template returns a template for IPv4 validation
func IPv4Template() TypeTemplate {
	return TypeTemplate{
		TypeName:       "str",
		FormatName:     "ipv4",
		Description:    "IPv4 address string",
		ValidationCode: `self.{property} == None or regex.match("^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$", self.{property}), "{property} must be a valid IPv4 address"`,
		Comments: []string{
			"# Format: ipv4",
			"# IPv4 address string",
		},
		SchemaContent: `schema IPv4Address:
    """IPv4 address string validation.
    
    Validates strings to ensure they conform to IPv4 address format.
    """
    value: str
    
    check:
        value == None or regex.match("^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$", value), "must be a valid IPv4 address"
`,
	}
}

